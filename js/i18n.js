// Language Application Logic
function setLanguage(lang) {
  // Save to localStorage
  localStorage.setItem("portfolio-lang", lang);

  // Update HTML lang attribute
  document.documentElement.lang = lang;

  // Get all elements that need translation
  const elementsToTranslate = document.querySelectorAll("[data-i18n]");

  elementsToTranslate.forEach((element) => {
    const key = element.getAttribute("data-i18n");

    // Check if the current language has the translation for the key
    if (translations[lang] && translations[lang][key]) {
      // se é um input placeholder
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translations[lang][key];
      } else {
        // if translation contains HTML (like <br>) we should use innerHTML
        if (
          translations[lang][key].includes("<") ||
          translations[lang][key].includes("&#")
        ) {
          element.innerHTML = translations[lang][key];

          // If it's the hero title, we need to append the Span and SVG back since it gets overwritten by innerHTML
          if (key === "hero-title-1") {
            element.innerHTML =
              translations[lang][key] +
              `
               <span class="highlight">Danilo Pérez</span>
               <svg class="title-shape" viewBox="0 0 200 20" xmlns="http://www.w3.org/2000/svg"><path d="M0,10 Q50,20 100,10 T200,10" fill="none" stroke="var(--clr-accent)" stroke-width="4" stroke-linecap="round"/></svg>
               `;
          }
        } else {
          // For text and buttons to not lose their inner icons, we need to check if they have icons
          const icon = element.querySelector("i");
          if (icon) {
            // if it's the hire button or others, keep the icon
            if (element.classList.contains("btn-outline")) {
              element.innerHTML = translations[lang][key] + " ";
              element.appendChild(icon);
            } else if (element.classList.contains("btn-primary")) {
              if (key === "contact-btn") {
                element.innerHTML = translations[lang][key] + " ";
                element.appendChild(icon);
              } else if (key === "skills-btn") {
                // Manter o ícone
                element.innerHTML = translations[lang][key] + " ";
                element.appendChild(icon);
              } else if (key === "hero-btn-portfolio") {
                element.innerHTML = translations[lang][key] + " ";
                element.appendChild(icon);
              } else {
                element.innerHTML = translations[lang][key];
              }
            } else if (element.classList.contains("btn-video")) {
              element.innerHTML = "";
              element.appendChild(icon);
              element.innerHTML += " " + translations[lang][key];
            } else {
              element.textContent = translations[lang][key];
            }
          } else {
            element.textContent = translations[lang][key];
          }
        }
      }
    }
  });

  // Atualiza Bandeira e Texto do Dropdown Principal
  const currentFlagMap = {
    pt: "fi-br",
    en: "fi-us",
    es: "fi-es",
  };
  const currentTextMap = {
    pt: "Português",
    en: "English",
    es: "Español",
  };

  const currentFlagEl = document.getElementById("current-lang-flag");
  const currentTextEl = document.getElementById("current-lang-text");

  if (currentFlagEl && currentTextEl) {
    currentFlagEl.className = `fi ${currentFlagMap[lang]}`;
    currentTextEl.textContent = currentTextMap[lang];
  }

  // Update Resume Viewer and Download Links if on resume.html
  const resumeIframe = document.querySelector(".resume-iframe");
  const downloadLink = document.querySelector(".download-actions .btn-primary");

  if (resumeIframe && downloadLink) {
    const pdfPath = `./assets/img/docs/curriculo-${lang}.pdf`;
    resumeIframe.src = pdfPath;

    // Update the fallback download link inside the iframe
    resumeIframe.innerHTML = `Seu navegador não suporta visualização de PDF. <a href="${pdfPath}">Clique aqui para baixar.</a>`;

    // Update the main download button
    downloadLink.href = pdfPath;

    // Set an appropriate download name
    const downloadNames = {
      pt: "Curriculo_Danilo_Alves_Perez.pdf",
      en: "Danilo_Alves_Perez_Resume.pdf",
      es: "Curriculo_Danilo_Alves_Perez_ES.pdf"
    }
    downloadLink.download = downloadNames[lang];
  }
}


// Initialize language on DOM Load
document.addEventListener("DOMContentLoaded", () => {
  // Check local storage or use default 'pt'
  const savedLang = localStorage.getItem("portfolio-lang") || "pt";
  setLanguage(savedLang);

  // Dropdown Logic
  const dropdownBtn = document.getElementById("lang-dropdown-btn");
  const dropdown = document.querySelector(".lang-dropdown");

  if (dropdownBtn && dropdown) {
    // Toggle dropdown open/close
    dropdownBtn.addEventListener("click", (e) => {
      e.stopPropagation();
      dropdown.classList.toggle("active");
    });

    // Close dropdown when outside click
    document.addEventListener("click", (e) => {
      if (!dropdown.contains(e.target)) {
        dropdown.classList.remove("active");
      }
    });
  }

  // Add Event Listeners to all lang options inside the dropdown
  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.addEventListener("click", (e) => {
      e.preventDefault();
      const lang = e.currentTarget.getAttribute("data-lang");
      setLanguage(lang);
      if (dropdown) dropdown.classList.remove("active");
    });
  });
});
